    private void addRegularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRegularActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String pname = name.getText();
        String id = idno.getText();
        double wei = Double.parseDouble(weight.getText());
        double total = (wei * 30);
        cost.setText(String.valueOf(total));
        String customer = "Regular";
        if(!"".equals(name)){
            try {
                Connection conn = Databases.getConnection();
                PreparedStatement ps;
                ps = conn.prepareStatement("insert into purchase(name, nationalid, weight, customer, cerial_type, cost) VALUES(?,?,?,?,?,?)");
                ps.setString(1, pname);
                ps.setString(2, id);
                ps.setString(3, String.valueOf(wei));
                ps.setString(4, customer);
                ps.setString(5, "beans");
                ps.setString(6, String.valueOf(total));
                ps.executeUpdate();
                cost.setText(String.valueOf(total));
                showTable();
            } catch (Exception ex) {
                System.out.println("Error: "+ ex);
                Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_addRegularActionPerformed

