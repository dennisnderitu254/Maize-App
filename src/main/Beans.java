    private void addbrokerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbrokerActionPerformed
        // TODO add your handling code here:
        String pname = bname.getText();
        String id = bidno.getText();
        double wei = Double.parseDouble(bweight.getText());
        double total = (wei * 30);
        cost.setText(String.valueOf(total));
        String customer = "Broker";
        if(!"".equals(bname)){

            try {
                Connection conn = Databases.getConnection();
                PreparedStatement ps;
                ps = conn.prepareStatement("insert into purchase(name, nationalid, weight, customer, cerial_type, cost) VALUES(?,?,?,?,?,?)");
                ps.setString(1, pname);
                ps.setString(2, id);
                ps.setString(3, String.valueOf(wei));                                                                                                                                                           
                ps.setString(4, customer);
                ps.setString(5, "beans");
                ps.setString(6, String.valueOf(total));
                ps.executeUpdate();
                bcost.setText(String.valueOf(total));
                showTable();
            } catch (Exception ex) {
                System.out.println("Error: "+ ex);
                Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_addbrokerActionPerformed

